import UIKit

extension UIFont {
    
    /// HelveticaNeue-Bold
    /// - Parameter size: font size
    /// - Returns: UIFont
    class func helveticaNeueBold(ofSize size: CGFloat) -> UIFont{
        return self.init(name:"HelveticaNeue-Bold", size: size)!
    }

    
}
